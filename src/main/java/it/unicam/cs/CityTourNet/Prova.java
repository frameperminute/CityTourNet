package it.unicam.cs.CityTourNet;

public class Prova {
    private int index;

    private String name;

    Prova(int index, String name) {
        this.index = index;
        this.name = name;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
